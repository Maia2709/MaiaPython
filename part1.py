# არა სიმბოლოები, რიცხვები, დიდი/პატარა ასოები (ლათინურად) თუ ქართულს შემოიტანს
# უნდა დაუწერო რომ “შეიყვანე მხოლოდ ლათინური ასოები”
# **უნდა გვქონდეს 4 ინფუთი - სიგრძე / რიცხვები უნდა თუ არა / დიდი და პატარა ასოები თუ
# უნდა / თუ უნდა სიმბოლოები
# **უნდა დაწეროთ ლოგიკა იმის მიხედვით რასაც აირჩევს მომხმარებელი და ბოლოს
# დაუგენერიროთ პაროლი.


def main():
    while True:
        sz = input("რამდენ სიმბოლოიანი პაროლი გინდათ: ")
        if sz.isdigit() and int(sz) > 0:
            sz = int(sz)
            break
        else:
            print("თქვენ არასწორი მნიშვნელობა შემოიტანეთ")
    use_digits = input("შეიყვანე ციფრები? (y/n): ").lower() == 'y'
    use_letters = input("შეიყვანე დიდი/პატარა ასოები? (y/n): ").lower() == 'y'
    use_symbols = input("შეიყვანე სიმბოლოები? (y/n): ").lower() == 'y'

    if use_letters:
        check = input("მხოლოდ ლათინური ასოებია? (y/n): ").lower()
        if check != 'y':
            print("შეიყვანე მხოლოდ ლათინური ასოები.")
            return
    chars = ""
    if use_letters:
        chars += string.ascii_letters
    if use_digits:
        chars += string.digits
    if use_symbols:
        chars += string.punctuation

    if not chars:
        print("მონაცემები არასწორია, პაროლის გენერირება შეუძლებელია.")
        return

    password = ''.join(random.choice(chars) for _ in range(sz))
    print("გენერირებული პაროლი:", password)

main()

